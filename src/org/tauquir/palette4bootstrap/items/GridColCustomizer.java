    /**
     * This method is used to listen to comboBox selections inside the ADVANCED
     * configuration tab
     */
    private void jComboBoxADVANCEDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxADVANCEDItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {    // Ignore ItemEvent.DESELECTED
            @SuppressWarnings("unchecked")
            javax.swing.JComboBox<String> jcb = (javax.swing.JComboBox) evt.getSource();
            if (jcb.getSelectedIndex() > 0) { // If "None" was not selected
                grid.putAttribute(jcb.getName(), jcb.getSelectedIndex());
            } else {    // "None" was selected in the dropdown, so remove this key from the attributes
                grid.removeAttribute(jcb.getName());
            }
            // Now we update the preview text box
            jTextArea2.setText(grid.generateBody());
        }
    }//GEN-LAST:event_jComboBoxADVANCEDItemStateChanged


