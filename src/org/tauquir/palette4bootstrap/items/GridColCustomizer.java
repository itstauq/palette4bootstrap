    private void jComboBoxBASICItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxBASICItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {    // Ignore ItemEvent.DESELECTED
            grid.clearAllAttributes(); // Remove all
            grid.putAttribute(col_size.getSelectedItem().toString(), col_weight.getSelectedIndex() + 1);
            jTextArea1.setText(grid.generateBody());
        }
    }//GEN-LAST:event_jComboBoxBASICItemStateChanged

