    private void responsiveCheckBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_responsiveCheckBoxItemStateChanged
        if (responsiveCheckBox.isSelected()) {
            image.addClass("img-responsive");
        } else {
            image.removeClass("img-responsive");
        }
        codeGenerated.setText(image.generateBody());
    }//GEN-LAST:event_responsiveCheckBoxItemStateChanged

    private void shapeSelectorItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_shapeSelectorItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
            if (!evt.getItem().equals("Normal")) {             // if "Normal" option was not selected
                image.addClass(evt.getItem().toString());
                codeGenerated.setText(image.generateBody());
            }
        } else if (evt.getStateChange() == java.awt.event.ItemEvent.DESELECTED) {
            image.removeClass(evt.getItem().toString());  // Remove the deselected item from the list
            codeGenerated.setText(image.generateBody());
        }
    }//GEN-LAST:event_shapeSelectorItemStateChanged

    private void openFileChooserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openFileChooserButtonActionPerformed
        int v = imgChooser.showOpenDialog(this);
        if (v == JFileChooser.APPROVE_OPTION) {
            File file = imgChooser.getSelectedFile();
            String relativePath = bsPaletteUtilities.findRelativePath(file.getAbsolutePath(), currentDirectory);
            srcTextField.setText(relativePath);
            image.setSrc(relativePath);
            codeGenerated.setText(image.generateBody());
        }
    }//GEN-LAST:event_openFileChooserButtonActionPerformed


