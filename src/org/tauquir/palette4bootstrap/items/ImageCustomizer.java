    public boolean showDialog() {
        dialogOK = false;
        jd = new JDialog();
        jd.setSize(this.getPreferredSize());
        jd.setIconImage(new BufferedImage(1, 1, BufferedImage.TYPE_INT_ARGB_PRE));
        jd.setTitle(NbBundle.getMessage(getClass(), "Customizer.InsertPrefix")
                + " " + NbBundle.getMessage(getClass(), "MEDIA.IMAGE.NAME"));
        jd.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
        jd.setDefaultCloseOperation(JDialog.HIDE_ON_CLOSE);
        jd.add(this);
        jd.setLocationRelativeTo(null);
        jd.setVisible(true);
        jd.addWindowListener(new WindowAdapter() {
            @Override
            public void windowDeactivated(WindowEvent e) {
                /* Reset every parameter of image object so that old values
                   does not appear on re-opening the dialog */
                image.setSrc("http://via.placeholder.com/350x150");
                image.setAlt("");
                image.setHeight("");
                image.setWidth("");
                image.removeAllClasses();
            }
        });
        return dialogOK;
    }

    private JFileChooser initFileChooser() {
        JFileChooser chooser = new JFileChooser();
        chooser.setApproveButtonText(NbBundle.getMessage(getClass(), "ImageCustomizer.FileChooser.ApproveButtonText"));
        chooser.setCurrentDirectory(new File(currentDirectory));
        chooser.setDialogTitle(NbBundle.getMessage(getClass(), "ImageCustomizer.FileChooser.Title"));
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        chooser.setFileFilter(new FileNameExtensionFilter("Image Files", "jpg", "png", "gif", "jpeg", "bmp", "svg", "ico"));
        return chooser;
    }

